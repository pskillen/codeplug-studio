import { UV5R_MINI_DEFAULT_ENCRSYM } from './constants.ts';

/** 20 four-byte symbols — NeonPlug TBL_ENCRYPT (same family as CHIRP). */
const TBL_ENCRYPT: readonly (readonly number[])[] = [
  [0x42, 0x48, 0x54, 0x20],
  [0x43, 0x4f, 0x20, 0x37],
  [0x41, 0x20, 0x45, 0x53],
  [0x20, 0x45, 0x49, 0x59],
  [0x4d, 0x20, 0x50, 0x51],
  [0x58, 0x4e, 0x20, 0x59],
  [0x52, 0x56, 0x42, 0x20],
  [0x20, 0x48, 0x51, 0x50],
  [0x57, 0x20, 0x52, 0x43],
  [0x4d, 0x53, 0x20, 0x4e],
  [0x20, 0x53, 0x41, 0x54],
  [0x4b, 0x20, 0x44, 0x48],
  [0x5a, 0x4f, 0x20, 0x52],
  [0x43, 0x20, 0x53, 0x4c],
  [0x36, 0x52, 0x42, 0x20],
  [0x20, 0x4a, 0x43, 0x47],
  [0x50, 0x4e, 0x20, 0x56],
  [0x4a, 0x20, 0x50, 0x4b],
  [0x45, 0x4b, 0x20, 0x4c],
  [0x49, 0x20, 0x4c, 0x5a],
];

/**
 * Symmetric XOR transform used for both encrypt and decrypt.
 * Skip XOR when symbol byte is space, or data is 0 / 0xFF / equal to symbol / equal to symbol^0xFF.
 */
export function uv5rMiniCrypt(
  buffer: Uint8Array,
  symbolIndex: number = UV5R_MINI_DEFAULT_ENCRSYM,
): Uint8Array {
  const sym = TBL_ENCRYPT[symbolIndex] ?? TBL_ENCRYPT[0]!;
  const out = new Uint8Array(buffer.length);
  for (let i = 0; i < buffer.length; i++) {
    const b = buffer[i]!;
    const s = sym[i % 4]!;
    const xor = s !== 0x20 && b !== 0 && b !== 0xff && b !== s && b !== (s ^ 0xff);
    out[i] = xor ? b ^ s : b;
  }
  return out;
}
